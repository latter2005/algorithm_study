#include <stdio.h>
int main() {
	int n, h, w;
	char ary[10][1001], res[101] = "????????????????????????????????????????????????????????????????????????????????????????????????????";
	scanf("%d%d%d", &n, &h, &w);
	for (int i = 0; i < h; i++)
		scanf("%s", ary[i]);
	for (int i = 0; i < h; i++) {
		for(int j=0;j<n;j++)
		for (int k = j * w; k < j*w + w; k++)
			if (ary[i][k] != '?')
				res[j] = ary[i][k];
	}
	res[n] = '\0';
	printf("%s", res);
}